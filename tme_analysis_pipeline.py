# --- PyTorch Autoencoder ---
print("\nTraining autoencoder for latent representation (60 epochs)...")

class TME_Autoencoder(nn.Module):
    def __init__(self, input_dim):
        super().__init__()
        self.encoder = nn.Sequential(
            nn.Linear(input_dim, 256), nn.ReLU(),
            nn.Dropout(0.2),
            nn.Linear(256, 64), nn.ReLU(),
            nn.Linear(64, 32),
        )
        self.decoder = nn.Sequential(
            nn.Linear(32, 64),  nn.ReLU(),
            nn.Linear(64, 256), nn.ReLU(),
            nn.Linear(256, input_dim), nn.Sigmoid(),
        )
    def forward(self, x):
        latent = self.encoder(x)
        return latent, self.decoder(latent)

X_tensor = torch.FloatTensor(X)
ae       = TME_Autoencoder(X.shape[1])
opt      = optim.Adam(ae.parameters(), lr=1e-3, weight_decay=1e-5)
loss_fn  = nn.MSELoss()

for epoch in range(60):
    opt.zero_grad()
    latent, recon = ae(X_tensor)
    loss = loss_fn(recon, X_tensor)
    loss.backward()
    opt.step()
    if epoch % 20 == 0:
        print(f"  Epoch {epoch:3d} | Reconstruction loss: {loss.item():.4f}")

latent_np, _ = ae(X_tensor)
adata_clean.obsm['X_latent'] = latent_np.detach().numpy()
